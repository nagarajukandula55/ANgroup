/**
 * Canonical document-numbering type list.
 *
 * CONSOLIDATION CONTEXT (see PROGRESS.md's "Known duplicates to consolidate"
 * — this was Phase 1 blocking item #1): the original repo had SIX separate
 * number-generating implementations (services/numbering.service.ts,
 * services/documentEngine.ts, services/invoice.service.ts,
 * services/sales.service.ts, services/purchaseOrder.service.ts,
 * lib/invoice/generateInvoiceNumber.ts) and FOUR separate counter-storage
 * models (Sequence, Counter, DocumentCounter, InvoiceSequence), plus THREE
 * different getFinancialYear() implementations that didn't even agree on
 * output format (one returned "2026-27", another "2627"). Two of the six
 * generators (sales.service.ts's `"INV-" + Date.now()` and
 * purchaseOrder.service.ts's `"PO-" + Date.now()`) and one more
 * (invoice.service.ts, via a `countDocuments()`-based sequence) were
 * actively unsafe: Date.now()-based numbers can collide on concurrent
 * requests within the same millisecond, and countDocuments()-based
 * sequences are a classic race condition (two concurrent creates can both
 * read count=N and both generate number N+1) AND silently reuse numbers
 * after any document is deleted. Neither respected the admin-configurable
 * prefix/format that DocumentNumberConfig (and its admin UI at
 * Settings > Document Numbers) already existed to control.
 *
 * This file/module replaces ALL of the above with exactly one engine
 * (numberingService.ts) backed by exactly one atomic counter collection
 * (NumberSequence.model.ts) reading its prefix/format from the existing
 * DocumentNumberConfig model — so the admin UI actually controls every
 * document type's numbering, not just invoices.
 *
 * DOCUMENT_TYPES here is the UNION of DocumentNumberConfig.DOCUMENT_TYPES
 * (the admin-configurable set, already covers most GST/finance documents)
 * and the extra types the old Sequence model's enum had that
 * DocumentNumberConfig didn't (PRODUCT, PRODUCT_VARIANT, VENDOR_PRODUCT,
 * STOCK_ADJUSTMENT, STOCK_TRANSFER, PRODUCTION_ORDER, BATCH,
 * CUSTOMER_ORDER, RECEIPT) — nothing from either prior list is dropped.
 */

