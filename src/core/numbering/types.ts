  // admin-configurable like everything else.
  "AGREEMENT",
  // Discovered during the same sweep: VENDOR and EMPLOYEE IDs were each
  // generated by 3+ independently-drifted implementations across different
  // routes (VendorProfile.vendorId used "VND-" with 4-digit padding in two
  // routes and "VEN-" with 3-digit padding in a third; EmployeeProfile
  // followed a similar pattern) — all via countDocuments(), which is
  // globally-scoped (not per-business) AND race-prone. Folded into one
  // configurable type each so there's exactly one format, admin-editable,
  // going forward.
  "VENDOR",
  "EMPLOYEE",
  // Discovered later, from a real user report: creating a new Business
  // still generated its businessCode via `BUS-${Date.now()}` in
  // businessBootstrap.service.ts — never migrated during the original
  // consolidation sweep above, so it was the one entity code left
  // completely outside admin control despite this whole system existing
  // specifically to fix that class of bug.
  "BUSINESS",
  // A vendor signup request needs a tracking number the moment it's
  // submitted, before any business (and therefore before VENDOR's
  // businessId-aware numbering config) has been assigned — see
  // api/vendors/apply/route.ts and VendorProfile.requestNumber.
  "VENDOR_REQUEST",
  // CRM call-lifecycle module (call entry -> job sheet -> closure — see
  // models/CrmCall.ts and models/CrmJobSheet.ts). CALL is the human-facing
  // reference on a CrmCall record; JOB_SHEET is the reference on a
  // CrmJobSheet created when a call is converted into actual work. Both are
  // admin-configurable per business through the same Settings > Document
  // Numbers UI as every other document type here, rather than inventing a
  // separate ad-hoc numbering scheme for CRM.
  "CALL",
  "JOB_SHEET",
  // Warehouse/service-center codes were previously manually typed in the
  // creation form with no numbering at all — added so they're generated
  // atomically and admin-configurable like every other document type here.
  "WAREHOUSE",
  "SERVICE_CENTER",
  // Store Front facility codes — same pattern as WAREHOUSE/SERVICE_CENTER
  // above, generated when a vendor's enableStoreFront toggle is switched on
  // (see VendorProfile.storeFrontId / /api/vendors/[id]'s PUT handler).
  "STORE_FRONT",
  // Vendor-created materials (added inline from the vendor product wizard's
  // BOM step, see api/vendor/materials/route.ts) -- the raw sequence from
  // this type is used to build "<vendorCode>-MAT-00001", not this type's
  // own INV-style formatted value, but it still needs a registered
  // DocumentType to get an atomic, per-vendor-scoped counter the same way
  // every other numbered entity in this file does.
  "MATERIAL",
  // CRM job-sheet closure (api/crm/jobsheets/[id]/close) generates a
  // SalesInvoice per job -- per explicit requirement, a GST-bearing
  // invoice and a non-GST invoice must NOT share one running series (a
  // business needs its GST-taxable invoice numbers to be their own
  // consecutive sequence for filing purposes, separate from zero-GST/
  // exempt bills). Reuses "INVOICE" for the GST case; this type is only
  // for the non-GST case.
  "NON_GST_INVOICE",
  // Same job-sheet-closure invoice as above, but for the case where the
  // customer entered a company name (a B2B customer) -- gets its own
  // running series, separate from the walk-in/individual (B2C) series,
  // per explicit requirement.
  "B2B_INVOICE",
  // The one document type from models/SalesDocument.ts's set that wasn't
  // already in this list (QUOTATION/DELIVERY_CHALLAN/CREDIT_NOTE/DEBIT_NOTE
